package day17_loops;

public class DoWhileExample {
    public static void main(String[] args) {
        do {
            System.out.println("This will print at least one time");

        }while (false);

        int a = 0;
        do {
            System.out.println(a);
            a++;

        }while (a<=10); // it will print out 11 times







    }
}
